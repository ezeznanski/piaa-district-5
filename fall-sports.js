const key = document.body.dataset.sport;
const sport = fallSports[key];
const external = ' target="_blank" rel="noopener"';

if (sport) {
  document.title = `${sport.title} | PIAA District V`;
  document.querySelector("#sport-title").textContent = sport.title;
  document.querySelector("#sport-icon").textContent = sport.icon;
  document.querySelector("#sport-description").textContent = sport.description;
  document.querySelector("#resource-title").textContent = `${sport.title} Resources`;
  document.querySelector("#classification-grid").innerHTML = sport.classes.map(item => `
    <article class="classification-card"><div class="class-label">${item[0]}</div><h3>${item[1]}</h3><p>${item[2]}</p><div class="resource-actions">
      ${item[3] ? `<a href="${item[3]}"${external}>Rankings <span>↗</span></a>` : ""}
      ${item[4] ? `<a href="${item[4]}"${external}>${item[0] === "Team" ? "Team Champion" : "Playoff Bracket"} <span>↗</span></a>` : ""}
      ${!item[3] && !item[4] ? '<span class="resource-status">See the official information below.</span>' : ""}
    </div></article>`).join("");

  const chairman = document.querySelector("#chairman-card");
  if (sport.chairman) {
    const c = sport.chairman;
    chairman.innerHTML = `<p class="eyebrow">Sport Chairman</p><h2>${c.name}</h2><p class="chairman-role">${c.role}${c.school ? `<br>${c.school}` : ""}</p><dl>
      <div><dt>Email</dt><dd><a href="mailto:${c.email}">${c.email}</a></dd></div>
      ${c.phone ? `<div><dt>Phone</dt><dd><a href="tel:${c.phone.replace(/\D/g, "")}">${c.phone}</a></dd></div>` : ""}
    </dl>`;
  } else {
    chairman.innerHTML = '<p class="eyebrow">Sport Contact</p><h2>Information pending</h2><p class="chairman-role">A current chairperson is not identified in the materials published on the legacy District V site.</p>';
  }

  document.querySelector("#document-grid").innerHTML = sport.documents.map((doc, index) => `
    <a class="document-card${index === 0 ? " featured" : ""}" href="${doc[4]}"${external}><span class="document-icon" aria-hidden="true">${doc[0]}</span><div><span class="document-type">${doc[1]}</span><h3>${doc[2]}</h3><p>${doc[3]}</p><span class="document-link">${doc[5]} ↗</span></div></a>`).join("");
}
